fun main() {
  // variable VAR
  var userName: String = "Areeb"
  userName = "Kazmi"
  println("Hello $userName!")

  // immutable variable = VAL
  val immutabe: String = "Zaeem"
  // immutabe = "Daniyal"
  println("Hello $immutabe")

  // Integer value var
  var age: Int = 19

  // Vars can be implicitly defined as well
  var str = "This is a String"
}
